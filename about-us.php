பல்கலைக்கழகத்திற்கும் கல்வியியற்கல்லூரிக்கும் மாணவர்கள் தெரிவுசெய்யப்படுகின்ற2014 ம் ஆண்டில் இப்பாடசாலையில் புதிய
அதிபராகப் பதவியேற்ற இப்பாடசாலையின் பழைய மாணவரும் முன்னாள் ஆசிரியருமான ஜனாப் யு.ஊ.றியாஸ் (ளுடுPளு- 1) என்பவரின் காலத்தில்
பாடசாலை வகை 1ஊ ஆக தரமுயர்த்தப்பட்டதுடன் பு.ஊ.நு.யுஃடு கலைப்பிரிவும் ஆரம்பிக்கப்பட்டது. னர்.தரம் 05 புலமைப்பரிசில்
