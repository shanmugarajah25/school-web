<h1>பாடசாலையின் சுருக்க வரலாறு</h1>

<p>
    ஆரம்ப கிராமமான தண்ணீரூற்று என்னும் ஊரில் பாடசாலையின் தேவையை உணர்ந்த எமது முன்னோரான ஜனாப் மீரான் கண்டு கபீப் முகம்மது
    என்பவர் தனது சொந்த நிலமான 40 பேர்ச் பரப்பளவுள்ள காணியில் ஊர்மக்களின் ஒத்துளைப்புடன் ஓலைக் கொட்டில் அமைத்து கிராம
    முன்னேற்ற சங்க ஆதரவுடன்1934 ம் ஆண்டளவில் உத்தியோகப்பற்றற்ற முறையில் பாடசாலையை ஆரம்பித்து தானே ஆசானாகவும் கற்பித்து
    வந்தார்.இவருடன் 'மனேஜர்' என அழைக்கப்படுகின்ற மீரசாஹிபு அஹமது லெப்பை என்பவரும் நிர்வாகப்பணியில் ஈடுபட்டுள்ளார்.
    காலப்போக்கில் மீரான் கண்டு நாகூரான் என்பவர் தனது 40 பேர்ச் காணியை சிறுதொகைப் பணத்திற்கு பாடசாலைக்காக வழங்கியுள்ளார்.
    இப்பாடசாலை 1936ம் ஆண்டளவில் உத்தியோக பூர்வமாக அரசால் பொறுப்பேற்கப்பட்டுள்ளது. இதன்போது 36 மாணவர்கள் காணப்பட்டனர் 1 –
    5 வரையான வகுப்புக்கள் காணப்பட்டன. முதலாவது அதிபராக செல்லமுத்தம்மா என்பவர் பணிபுரிந்தார்.
</p>

<p>
    காலப்போக்கில் மாணவர்களின் எண்ணிக்கை அதிகரிக்க அயல்காணிகள் சுவீகரிக்கப்பட்டு பாடசாலைக்காணி விஸ்தரிக்கப்பட்டதுடன்
    யு.ஊ.டு.யு.றஹீம் அதிபராக இருந்த காலத்தில் பு.ஊ.நு.ழுஃடு வரை வகுப்புக்கள் உயர்த்தப்பட்டு முஃதண்ணீரூற்று முஸ்லிம் மகா
    வித்தியாலயமாகவும் பாடசாலை தரம் உயர்த்தப்பட்டது .
</p>

<p>
    இந்நிலையில் இப்பகுதியில் ஏற்பட்ட அசாதாரண சூழ்நிலை காரணமாக இப்பிரதேச மக்கள் இடம்பெயர்ந்தமையால் 1990.07.20 ம் திகதி
    பாடசாலையும் மூடப்பட்டது.இக் காலத்தில் ஆசு.ஆ.சாஹுல் ஹமீது என்பவர் அதிபராகவும் அவருடன் 26 ஆசிரியர்களும் பணிபுரிந்தனர்.
</p>

<p>
    மீண்டும் நாட்டில் ஏற்பட்ட சமாதான சூழ்நிலையால் 21 வருடங்களின் பின் 2011.02.02 ம் திகதி பாடசாலை மீள ஆரம்பிக்கப்பட்டது.
    முதல் அதிபராக ஆசு.க.இராஜகுலசிங்கம் நியமிக்கப்பட்டார்;(தரம் 1 – 9 வரை). இதன்பின்னர் ஆசு.யோகதாஸ் என்பவர் அதிபராக
    இருந்த காலத்தில் தரம் 11 வரை வகுப்புகள் உயர்வடைந்தன.
</p>

<p>
    2014 ம் ஆண்டில் இப்பாடசாலையில் புதிய அதிபராகப் பதவியேற்ற இப்பாடசாலையின் பழைய மாணவரும் முன்னாள் ஆசிரியருமான ஜனாப்
    யு.ஊ.றியாஸ் (ளுடுPளு- 1) என்பவரின் காலத்தில் பாடசாலை வகை 1ஊ ஆக தரமுயர்த்தப்பட்டதுடன் பு.ஊ.நு.யுஃடு கலைப்பிரிவும்
    ஆரம்பிக்கப்பட்டது.
</p>

